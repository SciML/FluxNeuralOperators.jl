    @testset "legendre_filter" begin
        H0, H1, G0, G1, Φ1, Φ2 = NeuralOperators.legendre_filter(3)

        @test H0 ≈ [0.70710678 0.0 0.0;
                    -0.61237244 0.35355339 0.0;
                    0.0 -0.6846532 0.1767767]
        @test H1 ≈ [0.70710678 0.0 0.0;
                    0.61237244 0.35355339 0.0;
                    0.0 0.6846532 0.1767767]
